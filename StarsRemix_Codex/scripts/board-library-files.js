import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
export const projectRoot = resolve(scriptDirectory, "..");
export const dataPath = resolve(projectRoot, "src/game/board-library-data.json");
export const runtimePath = resolve(projectRoot, "src/game/board-library.js");
export const handmadeDirectory = resolve(projectRoot, "boards/handmade");

export async function readExistingLibrary() {
  try {
    const parsed = JSON.parse(await readFile(dataPath, "utf8"));
    if (!Array.isArray(parsed.boards)) throw new Error("Board library data is invalid.");
    return parsed;
  } catch (error) {
    if (error?.code === "ENOENT") return { version: 1, boards: [] };
    throw error;
  }
}

export async function readHandmadeBoards() {
  await mkdir(handmadeDirectory, { recursive: true });
  const filenames = (await readdir(handmadeDirectory))
    .filter((filename) => filename.endsWith(".json"))
    .sort();
  return Promise.all(filenames.map(async (filename) => {
    const entry = JSON.parse(await readFile(resolve(handmadeDirectory, filename), "utf8"));
    if (!entry?.puzzle?.id?.startsWith("handmade-")) {
      throw new Error(`${filename} does not contain a handmade board ID.`);
    }
    return entry;
  }));
}

export function mergeHandmadeBoards(library, handmadeBoards) {
  const generatedBoards = library.boards.filter((entry) =>
    !entry?.puzzle?.id?.startsWith("handmade-"),
  );
  const ids = new Set(generatedBoards.map((entry) => entry.puzzle.id));
  for (const entry of handmadeBoards) {
    if (ids.has(entry.puzzle.id)) throw new Error(`Duplicate board ID ${entry.puzzle.id}.`);
    ids.add(entry.puzzle.id);
  }
  return { version: 1, boards: [...generatedBoards, ...handmadeBoards] };
}

export async function readLibraryWithHandmade() {
  return mergeHandmadeBoards(await readExistingLibrary(), await readHandmadeBoards());
}

export async function writeLibrary(library) {
  const output = {
    version: library.version ?? 1,
    generatedAt: new Date().toISOString(),
    boards: library.boards,
  };
  await writeFile(dataPath, `${JSON.stringify(output, null, 2)}\n`);
  await writeFile(
    runtimePath,
    `// Generated by the StarsRemix board-library scripts.\n` +
      `globalThis.StarsRemixBoardLibrary = ${JSON.stringify(output)};\n`,
  );
  return output;
}

export async function rebuildLibrary() {
  return writeLibrary(await readLibraryWithHandmade());
}

export function boardSlug(value) {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 48) || "untitled";
}

export async function publishHandmadeBoard(entry) {
  const handmadeBoards = await readHandmadeBoards();
  const usedIds = new Set([
    ...(await readExistingLibrary()).boards.map((board) => board.puzzle.id),
    ...handmadeBoards.map((board) => board.puzzle.id),
  ]);
  const baseId = `handmade-${boardSlug(entry.puzzle.title)}`;
  let id = baseId;
  for (let suffix = 2; usedIds.has(id); suffix += 1) id = `${baseId}-${suffix}`;

  const published = {
    ...entry,
    puzzle: { ...entry.puzzle, id },
    source: "handmade",
  };
  await writeFile(resolve(handmadeDirectory, `${id}.json`), `${JSON.stringify(published, null, 2)}\n`, {
    flag: "wx",
  });
  const library = await rebuildLibrary();
  return { entry: published, librarySize: library.boards.length };
}
